<?php
require_once ROOT . '/core/Controller.php';
require_once ROOT . '/app/models/UserModel.php';

class AuthController extends Controller
{
    private UserModel $userModel;

    public function __construct()
    {
        $this->userModel = new UserModel();
    }

    /*
    |--------------------------------------------------------------------------
    | REGISTER
    |--------------------------------------------------------------------------
    */
    public function register(): void
    {
        /*
        |--------------------------------------------------------------------------
        | JIKA SUDAH LOGIN
        |--------------------------------------------------------------------------
        */
        if (isset($_SESSION['user'])) {

            $this->redirectByRole(
                $_SESSION['user']['role']
            );

            exit;
        }

        /*
        |--------------------------------------------------------------------------
        | PROSES REGISTER
        |--------------------------------------------------------------------------
        */
        if ($_SERVER['REQUEST_METHOD'] === 'POST') {

            $nama = trim(
                $_POST['nama'] ?? ''
            );

            $username = trim(
                $_POST['username'] ?? ''
            );

            $password = trim(
                $_POST['password'] ?? ''
            );

            /*
            |--------------------------------------------------------------------------
            | VALIDASI
            |--------------------------------------------------------------------------
            */
            if (
                $nama === '' ||
                $username === '' ||
                $password === ''
            ) {

                $this->view(
                    'auth/register',
                    [
                        'error' =>
                        'Semua field wajib diisi.'
                    ]
                );

                return;
            }

            /*
            |--------------------------------------------------------------------------
            | CEK USERNAME
            |--------------------------------------------------------------------------
            */
            $existing =
                $this->userModel
                ->findByUsername($username);

            if ($existing) {

                $this->view(
                    'auth/register',
                    [
                        'error' =>
                        'Username sudah digunakan.'
                    ]
                );

                return;
            }

            /*
            |--------------------------------------------------------------------------
            | CREATE USER
            |--------------------------------------------------------------------------
            */
            $this->userModel->create(
                $nama,
                $username,
                $password,
                'masyarakat'
            );

            /*
            |--------------------------------------------------------------------------
            | FLASH
            |--------------------------------------------------------------------------
            */
            $_SESSION['flash'] = [
                'type' => 'success',
                'msg'  =>
                'Registrasi berhasil, silakan login.'
            ];

            /*
            |--------------------------------------------------------------------------
            | REDIRECT LOGIN
            |--------------------------------------------------------------------------
            */
            header(
                'Location: ' .
                    BASE_URL .
                    'auth/login'
            );

            exit;
        }

        /*
        |--------------------------------------------------------------------------
        | VIEW REGISTER
        |--------------------------------------------------------------------------
        */
        $this->view('auth/register');
    }

    /*
    |--------------------------------------------------------------------------
    | LOGIN
    |--------------------------------------------------------------------------
    */
    public function login(): void
    {
        /*
        |--------------------------------------------------------------------------
        | JIKA SUDAH LOGIN
        |--------------------------------------------------------------------------
        */
        if (isset($_SESSION['user'])) {

            $this->redirectByRole(
                $_SESSION['user']['role']
            );

            exit;
        }

        $error = '';

        /*
        |--------------------------------------------------------------------------
        | PROSES LOGIN
        |--------------------------------------------------------------------------
        */
        if ($_SERVER['REQUEST_METHOD'] === 'POST') {

            $username = trim(
                $_POST['username'] ?? ''
            );

            $password = trim(
                $_POST['password'] ?? ''
            );

            /*
            |--------------------------------------------------------------------------
            | VALIDASI
            |--------------------------------------------------------------------------
            */
            if (
                $username === '' ||
                $password === ''
            ) {

                $error =
                    'Username dan password wajib diisi.';
            } else {

                /*
                |--------------------------------------------------------------------------
                | CEK USER
                |--------------------------------------------------------------------------
                */
                $user =
                    $this->userModel
                    ->findByUsername($username);

                /*
                |--------------------------------------------------------------------------
                | VERIFY PASSWORD
                |--------------------------------------------------------------------------
                */
                if (
                    $user &&
                    password_verify(
                        $password,
                        $user['password']
                    )
                ) {

                    /*
                    |--------------------------------------------------------------------------
                    | SESSION
                    |--------------------------------------------------------------------------
                    */
                    $_SESSION['user'] = [

                        'id'       => $user['id'],

                        'nama'     => $user['nama'],

                        'username' => $user['username'],

                        'role'     => $user['role']

                    ];

                    $_SESSION['user_id'] =
                        $user['id'];

                    $_SESSION['nama'] =
                        $user['nama'];

                    $_SESSION['username'] =
                        $user['username'];

                    $_SESSION['role'] =
                        $user['role'];

                    /*
                    |--------------------------------------------------------------------------
                    | FLASH
                    |--------------------------------------------------------------------------
                    */
                    $_SESSION['flash'] = [

                        'type' => 'success',

                        'msg'  =>
                        'Login berhasil.'

                    ];

                    /*
                    |--------------------------------------------------------------------------
                    | REDIRECT ROLE
                    |--------------------------------------------------------------------------
                    */
                    $this->redirectByRole(
                        $user['role']
                    );

                    exit;
                } else {

                    $error =
                        'Username atau password salah.';
                }
            }
        }

        /*
        |--------------------------------------------------------------------------
        | VIEW LOGIN
        |--------------------------------------------------------------------------
        */
        $this->view(
            'auth/login',
            [
                'error' => $error
            ]
        );
    }

    /*
    |--------------------------------------------------------------------------
    | LOGOUT
    |--------------------------------------------------------------------------
    */
    public function logout(): void
    {
        /*
        |--------------------------------------------------------------------------
        | HAPUS SESSION
        |--------------------------------------------------------------------------
        */
        $_SESSION = [];

        session_destroy();

        /*
        |--------------------------------------------------------------------------
        | REDIRECT HOME
        |--------------------------------------------------------------------------
        */
        header(
            'Location: ' .
                BASE_URL .
                'home'
        );

        exit;
    }

    /*
    |--------------------------------------------------------------------------
    | REDIRECT ROLE
    |--------------------------------------------------------------------------
    */
    private function redirectByRole(
        string $role
    ): void {

        switch ($role) {

            /*
            |--------------------------------------------------------------------------
            | ADMIN
            |--------------------------------------------------------------------------
            */
            case 'admin':

                header(
                    'Location: ' .
                        BASE_URL .
                        'dashboard/admin'
                );

                break;

            /*
            |--------------------------------------------------------------------------
            | DINAS
            |--------------------------------------------------------------------------
            */
            case 'dinas':

                header(
                    'Location: ' .
                        BASE_URL .
                        'dashboard/dinas'
                );

                break;

            /*
            |--------------------------------------------------------------------------
            | PIMPINAN
            |--------------------------------------------------------------------------
            */
            case 'pimpinan':

                header(
                    'Location: ' .
                        BASE_URL .
                        'dashboard/pimpinan'
                );

                break;

            /*
            |--------------------------------------------------------------------------
            | MASYARAKAT
            |--------------------------------------------------------------------------
            */
            default:

                header(
                    'Location: ' .
                        BASE_URL .
                        'dashboard'
                );

                break;
        }

        exit;
    }
}
